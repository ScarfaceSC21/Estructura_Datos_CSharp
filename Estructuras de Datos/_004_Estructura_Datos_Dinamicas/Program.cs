    Las estructuras dinámicas de datos se pueden dividir en dos grandes grupos:
        Lineales:
            Lists Enlazadas
            Pilas,
            Colas,
        No Lineales:
            Arboles
            Grafos
     
    Las estructuras dinámicas de datos se utilizan para almacenamiento de datos del mundo real que están cambiando constantemente.

